using EcommerceDay1.Models;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;

namespace EcommerceDay1.Configuration
{
    public class PaymentInformationConfiguration : IEntityTypeConfiguration<PaymentInformation>
    {
        void IEntityTypeConfiguration<PaymentInformation>.Configure(EntityTypeBuilder<PaymentInformation> builder)
        {
            //throw new NotImplementedException();
            builder.HasKey(p => p.PaymentID);

            builder.Property(p => p.PaymentMethod).HasMaxLength(50);
            builder.Property(p => p.CardNumber).HasMaxLength(20);
            builder.Property(p => p.CVV).HasMaxLength(5);
        }
    }
}
